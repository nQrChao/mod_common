    /**
     * 从一个预设的、更适合做昵称的字库中随机挑选汉字生成昵称
     * @param count 生成昵称的长度，默认为4
     */
    fun generatePresetNickname(count: Int = 4): String {
        val result = StringBuilder()
        val random = Random.Default
        for (i in 0 until count) {
            val randomIndex = random.nextInt(nicknameCharacters.size)
            result.append(nicknameCharacters[randomIndex])
        }
        return result.toString()
    }

    /**
     * 从 Unicode 汉字范围中随机挑选字符生成昵称
     * @param count 生成昵称的长度，默认为4
     */
    fun generateRandomChineseNickname(count: Int = 4): String {
        val result = StringBuilder()
        val random = Random.Default
        val min = 0x4E00 // 常用汉字起始
        val max = 0x9FA5 // 常用汉字结束
        for (i in 0 until count) {
            val codePoint = random.nextInt(max - min + 1) + min
            result.append(codePoint.toChar())
        }
        return result.toString()
    }
